#人生的不同阶段
age = 3

if age < 2 :
	print("He's a baby.")
elif 2<=age and age<4 :
	print("He is learning walking.")
elif 4<=age and age<13 :
	print("He is a child.")
elif 13<=age and age<20 :
	print("He is a young man.")
elif 20<=age and age<65 :
	print("He is a adult.")
else:
	print("He is a old man.")
